// 데이터베이스 타입
export interface Profile {
  id: string;
  email: string;
  full_name: string | null;
  is_admin: boolean;
  created_at: string;
  updated_at: string;
}

export interface SongRequest {
  id: string;
  phone_number: string;
  theme: Theme;
  answers: Record<string, string>;
  style: Style;
  genre: Genre;
  status: "pending" | "processing" | "completed" | "sent";
  sent_at: string | null;
  created_at: string;
  updated_at: string;
}

export interface SongPrompt {
  id: string;
  request_id: string;
  song_title: string;
  lyrics: string;
  style_tags: string;
  created_at: string;
}

